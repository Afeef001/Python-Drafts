# A circle pattern with triangles

import turtle

turtle.speed(0)
turtle.hideturtle()
turtle.bgcolor("Black")
turtle.color("blue","skyblue")
# turtle.begin_fill()


for i in range(12*2):

	turtle.forward(100)
	turtle.left(30)
	turtle.forward(100)
	turtle.left(165)
	turtle.forward(195)
# turtle.end_fill()

turtle.done()