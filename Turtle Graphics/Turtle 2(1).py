# A circle pattern with triangles

import turtle

turtle.speed(5)
turtle.hideturtle()
turtle.bgcolor("Black")
turtle.color("Lime","Lime")
# turtle.begin_fill()


for i in range(12*2):

	turtle.forward(100)

	turtle.left(30)
	turtle.forward(100)
	turtle.right(130)
	turtle.forward(150)
# turtle.end_fill()

turtle.done()








